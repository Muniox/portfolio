function inside(x, y, poly) {
  let hit = false;
  for (let i = 0, j = poly.length - 1; i < poly.length; j = i++) {
    const xi = poly[i][0], yi = poly[i][1];
    const xj = poly[j][0], yj = poly[j][1];
    if ((yi > y) !== (yj > y) && x < (xj - xi) * (y - yi) / (yj - yi) + xi) hit = !hit;
  }
  return hit;
}

function isLand(lon, lat) {
  for (const poly of lands) {
    if (inside(lon, lat, poly)) return true;
  }
  return false;
}

// ── Orthographic (globe) projection ──
// Centered on Poland: 20°E, 50°N
const lon0 = 15 * Math.PI / 180;
const lat0 = 15 * Math.PI / 180;
const R = 308;           // globe radius in px
const size = R * 2 + 60; // svg size
const cx = size / 2;
const cy = size / 2;
const step = 1.8;        // degrees between sample points
const baseDotR = 1.3;

function toRad(d) { return d * Math.PI / 180; }

// Orthographic projection: returns {x, y, visible, depth}
function project(lon, lat) {
  const lam = toRad(lon);
  const phi = toRad(lat);
  const cosC = Math.sin(lat0) * Math.sin(phi) + Math.cos(lat0) * Math.cos(phi) * Math.cos(lam - lon0);
  if (cosC < 0) return null; // behind globe
  const x = cx + R * Math.cos(phi) * Math.sin(lam - lon0);
  const y = cy - R * (Math.cos(lat0) * Math.sin(phi) - Math.sin(lat0) * Math.cos(phi) * Math.cos(lam - lon0));
  return { x, y, depth: cosC };
}

let dots = '';
let count = 0;

for (let lat = 85; lat >= -65; lat -= step) {
  for (let lon = -180; lon < 180; lon += step) {
    if (!isLand(lon, lat)) continue;
    const p = project(lon, lat);
    if (!p) continue;
    // Dots near edge are smaller and more transparent
    const r = (baseDotR * (0.5 + 0.5 * p.depth)).toFixed(2);
    const op = (0.3 + 0.7 * p.depth).toFixed(2);
    dots += `<circle cx="${p.x.toFixed(1)}" cy="${p.y.toFixed(1)}" r="${r}" opacity="${op}"/>\n`;
    count++;
  }
}


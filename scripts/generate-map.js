function generateGridLines(cx, cy, R, lon0, lat0) {
  let lines = '';
  // Latitude lines every 20°
  for (let lat = -60; lat <= 80; lat += 20) {
    let path = '';
    for (let lon = -180; lon <= 180; lon += 2) {
      const p = project(lon, lat);
      if (!p) { if (path) { lines += `<path d="${path}"/>\n`; path = ''; } continue; }
      path += path ? `L${p.x.toFixed(1)} ${p.y.toFixed(1)}` : `M${p.x.toFixed(1)} ${p.y.toFixed(1)}`;
    }
    if (path) lines += `<path d="${path}"/>\n`;
  }
  // Longitude lines every 30°
  for (let lon = -180; lon < 180; lon += 30) {
    let path = '';
    for (let lat = -80; lat <= 80; lat += 2) {
      const p = project(lon, lat);
      if (!p) { if (path) { lines += `<path d="${path}"/>\n`; path = ''; } continue; }
      path += path ? `L${p.x.toFixed(1)} ${p.y.toFixed(1)}` : `M${p.x.toFixed(1)} ${p.y.toFixed(1)}`;
    }
    if (path) lines += `<path d="${path}"/>\n`;
  }
  return lines;
}

